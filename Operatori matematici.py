# Operatori di assegnazione
# per attribuire un nome a un dato oggetto

# = Operatore di assegnazione di base
x = 4+5
y = 6
# += Aggiunge un elemento e assegna il risultato al nome
x+= y # (x = x + y )

# -= Sottrae un elemento e assegna il risultato al nome
x-= y # (x = x - y )
# /= Divide un elemento e assegna il risultao al nome
x/= y # (x = x/y )
# *= Moltiplica un elemento e assegna il risultato al nome
x*= y # (x = x % y )
# %= modulo e riassegnazione
x%= y # (x = x % y, ossia ilresto della divisione )
# **= Potenza e riassegnazione
x**= y # (x = x ** y )
# //= Floor divisione e riassegnazione
x//= y # (x = x // y , dvisione arrotondata per difetto, quindi toglie solo la virgola)

